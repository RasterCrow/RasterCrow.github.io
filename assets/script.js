$(document).ready(function () {
	// Get the navbar
	var navbar = document.getElementById("navbar");
	var content = document.getElementById("content");
	// Get the offset position of the navbar
	var sticky = navbar.offsetTop;
	//get if device is mobile
	var ua = navigator.userAgent;
	//var isMobile = /Android|webOS|iPhone|iPad|iPod/i.test(ua);
	var isMobile = false
	//console.log(ua)
	//console.log(isMobile)

	//on scroll add stickynavbar + navbar background
	window.onscroll = function () { addStickyNavbar() };

	//first translate in italian
	translatePage("it");

	$('.post-modal').on('click', function (e) {
		e.preventDefault();
		$('#modalPost').modal('show').find('.modal-content').load($(this).attr('href'));
	});
	//make collpasible elemnts not collpase after opening
	$('#collapseInfo').on('shown.bs.collapse', function () {
		document.getElementById("infoTitle").removeAttribute("data-toggle")
	})
	$('#collapsePortfolio').on('shown.bs.collapse', function () {
		document.getElementById("portfolioTitle").removeAttribute("data-toggle")
	})
	$('#collapseContacts').on('shown.bs.collapse', function () {
		document.getElementById("contactsTitle").removeAttribute("data-toggle")
	})
	$('#collapseBlog').on('shown.bs.collapse', function () {
		document.getElementById("blogTitle").removeAttribute("data-toggle")
	})

	//change color to navbar section links
	$("#blogTitle").on("click", function () {
		$(this).toggleClass('button-clicked');
	})
	$("#contactsTitle").on("click", function () {
		$(this).toggleClass('button-clicked');
	})
	$("#infoTitle").on("click", function () {
		$(this).toggleClass('button-clicked');
	})
	$("#portfolioTitle").on("click", function () {
		$(this).toggleClass('button-clicked');
	})

	//scroll into the collapse clicked
	$(document).on('shown.bs.collapse', function (event) {
		//console.log(event);
		event.target.scrollIntoView({ behavior: 'smooth', block: 'center' })
	});

	//more info button  for info section
	$("#moreinfoButton").on("click", function () {
		document.getElementById("moreinfoButton").hidden = true;
	})

	// Add the sticky class to the navbar when you reach its scroll position. Remove "sticky" when you leave the scroll position
	function addStickyNavbar() {
		if (!isMobile) {
			if (window.pageYOffset >= sticky) {
				navbar.classList.add("sticky")
				navbar.classList.add("trasparent-background")
				content.classList.add("content")
			} else {
				navbar.classList.remove("sticky");
				navbar.classList.remove("trasparent-background")
				//Used to get animation when removing the trasparent background
				navbar.classList.add("not_trasparent-background")
				content.classList.remove("content")
			}
		}
	}

	//change language with radio button
	$("input[type='radio']").click(function () {
		var pageLanguage = $("input[name='language']:checked").val();
		translatePage(pageLanguage);
	});

	//load translation
	function translatePage(language) {
		document.getElementById("infoText").innerHTML = text[language].info;
		document.getElementById("portfolioText").innerHTML = text[language].portfolio.summary;
		document.getElementById("slide1Title").innerHTML = text[language].portfolio.slide1.title;
		document.getElementById("slide1Text").innerHTML = text[language].portfolio.slide1.desc;
		$('#slide2 h5').text(text[language].portfolio.slide2.title);
		$('#slide2 p').text(text[language].portfolio.slide2.desc);
		$('#slide3 h5').text(text[language].portfolio.slide3.title);
		document.getElementById("slide3Text").innerHTML = text[language].portfolio.slide3.desc;
		document.getElementById("slide4Title").innerHTML = text[language].portfolio.slide4.title;
		document.getElementById("slide4Text").innerHTML = text[language].portfolio.slide4.desc;
		document.getElementById("slide5Title").innerHTML = text[language].portfolio.slide5.title;
		document.getElementById("slide5Text").innerHTML = text[language].portfolio.slide5.desc;
		$('#contactsTitle').text(text[language].contacts.title);
		document.getElementById("contactsText").innerHTML = text[language].contacts.summary;
		document.getElementById("blogAlert").innerHTML = text[language].blogAlert;
		document.getElementById("moreinfoButton").innerHTML = text[language].infoExpandButton;
		document.getElementById("moreInfoText").innerHTML = text[language].infoExpanded;
	};
});
